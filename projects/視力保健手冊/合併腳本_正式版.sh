# 摘要

## 背景

台灣學童近視率高居全球之冠，高中生近視率超過 80%，高度近視率超過 35%。民國 88 年起推動之散瞳劑政策歷經 25 年，近視率未見顯著改善。

## 目的

提供以實證為基礎的視力保健建議，檢討現行政策，並提出可行的改革方向。

## 主要發現

1. **戶外活動**：每天 ≥2 小時可減少 46% 新發近視（證據等級：高）
2. **阿托品**：0.05% 是目前國際共識的最佳濃度
3. **遠視儲備**：適量即可（+1.00D 左右），過高反而有斜弱視風險
4. **現行轉介機制**：存在「眼科塞車」與「6 歲以下檢測亂象」等問題

## 主要建議

1. 推動「預防為主、階梯式控制」策略
2. 重視戶外活動的預防效果
3. 落實專業分工與跨專業合作
4. 加強 6 歲以下兒童視力檢查的品質監督
5. 發揮驗光師在公衛體系中的角色

## 關鍵詞

近視控制、遠視儲備、阿托品、戶外活動、驗光師、學童視力

